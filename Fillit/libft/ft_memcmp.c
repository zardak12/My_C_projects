#include "libft.h"

int				ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t		i;
	t_cuc		*str1;
	t_cuc		*str2;

	i = 0;
	str1 = (t_cuc *)s1;
	str2 = (t_cuc *)s2;
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}
